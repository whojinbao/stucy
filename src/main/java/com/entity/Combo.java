package com.entity;

public class Combo {
	private String comboType ;
	private String  comboContent ;
	private String  comboTime ;
	private String  comboMoney ;
	public String getComboType() {
		return comboType;
	}
	public void setComboType(String comboType) {
		this.comboType = comboType;
	}
	public String getComboContent() {
		return comboContent;
	}
	public void setComboContent(String comboContent) {
		this.comboContent = comboContent;
	}
	public String getComboTime() {
		return comboTime;
	}
	public void setComboTime(String comboTime) {
		this.comboTime = comboTime;
	}
	public String getComboMoney() {
		return comboMoney;
	}
	public void setComboMoney(String comboMoney) {
		this.comboMoney = comboMoney;
	}
	
}
