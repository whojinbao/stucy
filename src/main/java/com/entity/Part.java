package com.entity;

public class Part {
	private String partId ;
	private String partName ;
	private String partMark ;
	private String state ;
	private String partDescribe ;
	public String getPartDescribe() {
		return partDescribe;
	}
	public void setPartDescribe(String partDescribe) {
		this.partDescribe = partDescribe;
	}
	public String getPartMark() {
		return partMark;
	}
	public void setPartMark(String partMark) {
		this.partMark = partMark;
	}
	public String getPartId() {
		return partId;
	}
	public void setPartId(String partId) {
		this.partId = partId;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
}
