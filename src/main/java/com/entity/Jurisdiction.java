package com.entity;

public class Jurisdiction {
	private String jurId ;
	private String jurName ;
	private String jurDescribe ;
	private String jurStatus ;
	public String getJurId() {
		return jurId;
	}
	public void setJurId(String jurId) {
		this.jurId = jurId;
	}
	public String getJurName() {
		return jurName;
	}
	public void setJurName(String jurName) {
		this.jurName = jurName;
	}
	public String getJurDescribe() {
		return jurDescribe;
	}
	public void setJurDescribe(String jurDescribe) {
		this.jurDescribe = jurDescribe;
	}
	public String getJurStatus() {
		return jurStatus;
	}
	public void setJurStatus(String jurStatus) {
		this.jurStatus = jurStatus;
	}
	
	
}
